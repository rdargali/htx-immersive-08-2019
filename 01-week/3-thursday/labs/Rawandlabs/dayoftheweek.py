day = int(input('What day of the week is it (0-6)?'))
if day == 0:
    print ('It\'s Sunday!')
elif day == 1: 
    print ('It\'s Monday!')
elif day == 2:
    print ('It\'s Tuesday!')
elif day == 3:
    print ('It\'s Wednesday!')
elif day == 4:
    print ('It\'s Thursday!')
elif day == 5:
    print ('It\'s Friday!')
elif day == 6:
    print ('It\'s Saturday!')